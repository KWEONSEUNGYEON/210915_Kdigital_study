-- 현재 우리는 system 권한으로 데이터베이스에 접속을 했다.
/*
데이터베이스, 스키마, 테이블

데이터베이스(=dbms랑 거의 비슷한 의미) : 프로그램 전반을 의미
스키마(=유저랑 거의 비슷한 의미) : 테이블, 뷰(=가상테이블) 등을 모아놓은 것 
테이블 : 우리가 일반적으로 말하는 데이터베이스 (= 학생데이터베이스, 포켓몬 데이터베이스 등....)
키를 가지고, 키를 비롯한 속성을 갖고, 데이터들을 저장하고 삭제하고 조회하고 수정하는 등....
데이터를 저장하는 것을 '테이블'이라고 함.


유저를 새로 만든다라는 건 새로운 스키마를 하나 만드는 것과 거의 동일한 의미
이 스키마 안에 새로운 테이블들을 삽입할 것이다

단, 최고권한 등일 경우에는 다른 유저의 테이블들에 접근이 가능
*/


/*
system권한(=최고권한)말고 일반유저 권한으로 접속을 하고 싶다.
c##scott : 내가 만든 id
tiger : 내가 만든 id의 비밀번호
*/

-- id:c##scott, pw:tiger
-- 참고로 oracle 12c부터는 id앞에 반드시 c##이 무조건 붙어야 됨
create user c##scott identified by tiger;

--기본적인 권한을 부여
grant connect, resource to c##scott;

-- 유저에 대해서 용량 제한을 풀은 것
-- 이거 안 하면 제대로된 crud를 할 수 없다.
ALTER USER c##scott DEFAULT TABLESPACE USERS QUOTA UNLIMITED ON USERS;
